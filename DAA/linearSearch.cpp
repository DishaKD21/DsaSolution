#include <iostream>
using namespace std;

int linearSearch(int arr[], int n, int target)
{
    for (int i = 0; i < n; ++i)
    {
        if (arr[i] == target)
            return i; 
    }
    return -1; 
}

int main()
{
    int n;
    cin >> n;
    int arr[n];
    for (int i = 0; i < n; i++)
    {
        cin >> arr[i];
    }
    int target;
    cin>>target;
    int index = linearSearch(arr, n, target);
    if (index != -1)
        cout << "Found at index: " << index <<endl;
    else
        cout << "Not found";
    return 0;
}
